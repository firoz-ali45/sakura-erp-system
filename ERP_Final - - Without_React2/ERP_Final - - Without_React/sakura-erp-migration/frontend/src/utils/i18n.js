// Translation function
export function translate(key, lang = null) {
    const currentLang = lang || localStorage.getItem('portalLang') || 'en';
    return i18n[currentLang]?.[key] || key;
}

// Translate page function (like in index.html)
export function translatePage(lang) {
    if (!i18n[lang]) {
        console.error('❌ i18n[lang] is not available for lang:', lang);
        return;
    }

    // Set language and direction
    const isRTL = lang === 'ar';
    document.documentElement.lang = lang;
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.body.dir = isRTL ? 'rtl' : 'ltr';

    // Add/remove RTL class
    if (isRTL) {
        document.body.classList.add('rtl-mode');
        document.body.classList.remove('ltr-mode');
    } else {
        document.body.classList.add('ltr-mode');
        document.body.classList.remove('rtl-mode');
    }

    // Translate all elements with data-key attribute
    const elements = document.querySelectorAll('[data-key]');
    let translatedCount = 0;
    elements.forEach(el => {
        const key = el.getAttribute('data-key');
        if (i18n[lang] && i18n[lang][key]) {
            const translation = i18n[lang][key];
            
            // Check if element has child elements (like <span>, <i>, etc.)
            const hasChildElements = el.children.length > 0;
            
            if (hasChildElements) {
                // Element has child elements - need to preserve structure
                // Find all direct text nodes (not inside child elements)
                const directTextNodes = Array.from(el.childNodes).filter(node => 
                    node.nodeType === 3 && node.textContent.trim()
                );
                
                if (directTextNodes.length > 0) {
                    // Replace first text node, remove others
                    directTextNodes[0].textContent = translation;
                    for (let i = 1; i < directTextNodes.length; i++) {
                        directTextNodes[i].remove();
                    }
                } else {
                    // No direct text nodes - check if we should replace innerHTML
                    // Only replace if the current text matches a known translation
                    const currentText = el.textContent.trim();
                    // Check if current text is from English or Arabic
                    const isEnglishText = i18n['en'] && i18n['en'][key] && currentText === i18n['en'][key];
                    const isArabicText = i18n['ar'] && i18n['ar'][key] && currentText === i18n['ar'][key];
                    
                    if (isEnglishText || isArabicText) {
                        // Current text is a translation, replace entire content
                        el.innerHTML = translation;
                    } else {
                        // Unknown text, try to preserve structure
                        // Insert translation as first text node
                        const textNode = document.createTextNode(translation);
                        el.insertBefore(textNode, el.firstChild);
                    }
                }
            } else {
                // No children, simple text replacement
                el.textContent = translation;
            }
            translatedCount++;
        }
    });
    console.log(`✅ Translated ${translatedCount} elements with data-key`);

    // Also translate elements with data-lang attribute (for compatibility)
    const dataLangElements = document.querySelectorAll('[data-lang]');
    dataLangElements.forEach(el => {
        const key = el.getAttribute('data-lang');
        if (i18n[lang] && i18n[lang][key]) {
            el.innerHTML = i18n[lang][key];
        }
    });

    // Update all iframes with language change
    const iframes = document.querySelectorAll('iframe');
    iframes.forEach((iframe) => {
        try {
            if (iframe.contentWindow) {
                iframe.contentWindow.postMessage({ type: 'SET_LANGUAGE', lang }, '*');
                iframe.contentWindow.postMessage({ type: 'LANGUAGE_CHANGE', language: lang }, '*');
            }
        } catch (error) {
            console.warn('Could not send language message to iframe:', error);
        }
    });
}

// Broadcast language change (like in index.html)
export function broadcastLanguageChange(lang) {
    console.log('🌐 Language change requested:', lang);
    
    // Avoid redundant work if language is unchanged
    const prevLang = localStorage.getItem('portalLang') || 'en';
    if (prevLang === lang) {
        console.log('⏸️ Language already set to', lang, '- skipping re-translate');
        return;
    }

    // Update localStorage
    localStorage.setItem('portalLang', lang);
    window.currentLang = lang;
    
    console.log('✅ Language updated to:', lang);

    // Translate page
    translatePage(lang);
}

